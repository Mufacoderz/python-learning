# Variabel
x = 10
y = 3
z = 2.5

#cntoh opertor aritmatika
print("--- OPERATOR ARITMATIKA ---")
print("x + y =", x + y)
print("x - y =", x - y)
print("x * y =", x * y)
print("x / y =", x / y)
print("x // y =", x // y)
print("x % y =", x % y)
print("x ** y =", x ** y)

#contoh operator perbandingan
print("\n--- OPERATOR PERBANDINGAN ---")
print("x == y :", x == y)
print("x != y :", x != y)
print("x > y  :", x > y)
print("x < y  :", x < y)
print("x >= y :", x >= y)
print("x <= y :", x <= y)

#contoh penggunaan kurung
print("\n--- PENGARUH TANDA KURUNG ---")
print("10 + 2 * 3 =", 10 + 2 * 3)
print("(10 + 2) * 3 =", (10 + 2) * 3)

#contoh operator logika
print("\n--- OPERATOR LOGIKA ---")
print("x > 5 and y < 5 :", x > 5 and y < 5)
print("x > 5 or y > 5  :", x > 5 or y > 5)
print("not(x > y)      :", not(x > y))